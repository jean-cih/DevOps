#!/bin/bash

if [[ $# -eq 0 ]]; then
    echo "Необходимо ввести один параметр"
    exit 1
fi

if [[ $# -ne 1 ]]; then
    echo "Скрипту необходим ровно один параметр"
    exit 1
fi

if [[ $1 =~ ^[+-]?[0-9]+([.][0-9]+)?$ ]]; then
    echo "Некорректный ввод"
    exit 1
fi

echo "Параметр: $1"
exit 0
